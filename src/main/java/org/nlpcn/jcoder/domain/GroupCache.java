public class GroupCache {

	private String timeMD5;
	private String groupMD5;
	private String pomMD5;

	public String getTimeMD5() {
		return timeMD5;
	}

	public void setTimeMD5(String timeMD5) {
		this.timeMD5 = timeMD5;
	}

	public String getGroupMD5() {
		return groupMD5;
	}

	public void setGroupMD5(String groupMD5) {
		this.groupMD5 = groupMD5;
	}

	public String getPomMD5() {
		return pomMD5;
	}

	public void setPomMD5(String pomMD5) {
		this.pomMD5 = pomMD5;
	}


}
